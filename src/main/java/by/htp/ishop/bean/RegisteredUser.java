package by.htp.ishop.bean;

public class RegisteredUser {
	public String name;

	public RegisteredUser() {
		super();
	}

	public RegisteredUser(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
